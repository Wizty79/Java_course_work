public class BooleanComparisons {
    public static void main(String[] args) {
        int chemistryGrade = 95;
        int biologyGrade = 75;

        System.out.println(biologyGrade > chemistryGrade); //result is false, a boolean value 

        double sales = 37.55;
        double costs = 5.55;

        System.out.println(sales > costs);
        
    }
}
